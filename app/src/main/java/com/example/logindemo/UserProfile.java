package com.example.logindemo;

public class UserProfile {

    public String ditelindja;
    public String email;
    public String emri;
    public String mbiemri;
    public String gjaku;
    public String qyteti;
    public String celular;



    public UserProfile(String ditelindja, String email, String emri, String mbiemri, String gjaku, String qyteti, String celular) {
        this.ditelindja = ditelindja;
        this.email = email;
        this.emri = emri;
        this.mbiemri = mbiemri;
        this.gjaku= gjaku;
        this.qyteti= qyteti;
        this.celular =celular;
    }
}
